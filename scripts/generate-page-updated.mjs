import { execFileSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const OUTPUT_PATH = path.join(ROOT, "src/data/page-updated.ts");

const ROUTES = [
  { route: "/", sources: ["src/app/page.tsx"] },
  { route: "/about", sources: ["src/app/about/page.tsx"] },
  { route: "/blog", sources: ["src/app/blog/page.tsx", "src/app/blog/BlogIndexClient.tsx", "src/data/blog-tags.ts", "src/data/content.ts"] },
  { route: "/favorites", sources: ["src/app/favorites/page.tsx", "src/data/content.ts"] },
  { route: "/links", sources: ["src/app/links/page.tsx"] },
  { route: "/portfolio", sources: ["src/app/portfolio/page.tsx"] },
  { route: "/projects", sources: ["src/app/projects/page.tsx", "src/components/content/ProjectArchive.tsx", "src/data/content.ts"] },
  { route: "/travels", sources: ["src/app/travels/page.tsx", "src/data/content.ts"] },
  { route: "/travels/china-24", sources: ["src/app/travels/china-24/page.tsx", "src/components/travel/TravelDetailPage.tsx", "src/data/travel-details/china-24.json"] },
  { route: "/travels/japan-24", sources: ["src/app/travels/japan-24/page.tsx", "src/components/CollageLayout.tsx", "src/components/CollageCanvas.tsx", "src/data/travels/japan-24.json"] },
  { route: "/travels/netherlands-25", sources: ["src/app/travels/netherlands-25/page.tsx", "src/data/content.ts"] },
];

function git(args) {
  return execFileSync("git", args, {
    cwd: ROOT,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "ignore"],
  }).trim();
}

function isDirty(sources) {
  try {
    return git(["status", "--porcelain", "--", ...sources]).length > 0;
  } catch {
    return false;
  }
}

function lastCommittedDate(sources) {
  try {
    const value = git(["log", "-1", "--format=%cs", "--", ...sources]);
    return value || null;
  } catch {
    return null;
  }
}

function formatLabel(date) {
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  const year = String(date.getFullYear()).slice(-2);
  return `UPDATED ${month} · ${day} · ${year}`;
}

function dateForRoute(sources) {
  if (isDirty(sources)) {
    return new Date();
  }

  const committedDate = lastCommittedDate(sources);
  if (committedDate) {
    return new Date(`${committedDate}T00:00:00`);
  }

  return new Date();
}

const entries = Object.fromEntries(
  ROUTES.map(({ route, sources }) => [route, formatLabel(dateForRoute(sources))]),
);

const fallback = entries["/"] ?? formatLabel(new Date());
const body = `// Generated by scripts/generate-page-updated.mjs. Do not edit by hand.

export const PAGE_UPDATED = ${JSON.stringify(entries, null, 2)} as const;

export type PageUpdatedRoute = keyof typeof PAGE_UPDATED;

export const DEFAULT_UPDATED_LABEL = ${JSON.stringify(fallback)};

export function getPageUpdatedLabel(pathname: string | null | undefined) {
  if (!pathname) {
    return DEFAULT_UPDATED_LABEL;
  }

  const normalized = pathname === "/" ? "/" : pathname.replace(/\\/$/, "");
  return PAGE_UPDATED[normalized as PageUpdatedRoute] ?? DEFAULT_UPDATED_LABEL;
}
`;

mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
writeFileSync(OUTPUT_PATH, body);
